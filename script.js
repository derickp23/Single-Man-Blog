/* Javascript for button to hide quotes */
function hidequotes() {
    var x = document.getElementById("Relationshipquotes");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";}}


 function dropquotes() {
    var x = document.getElementById("InspirationalQuote");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";}}       




        

       